package p;

public class Student {
    private String name;
    private int age, salary;

    public void setName(String a) {
        name = a;
    }

    public String getName() {
        return name;
    }

    public void setAge(int b) {
        age = b;
    }

    public int getAge() {
        return age;
    }

    public void setSalary(int c) {
        salary = c;
    }

    public int getSalary() {
        return salary;
    }
}